-- Partitioned table by month (ready version uses posts_range to avoid clashing with baseline.posts)
CREATE TABLE IF NOT EXISTS posts_range (
  id BIGINT,
  user_id BIGINT,
  created_at TIMESTAMP,
  content TEXT
) PARTITION BY RANGE (created_at);

